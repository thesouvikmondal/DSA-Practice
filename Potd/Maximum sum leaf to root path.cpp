class Solution{

    struct Node {
    int data;
    Node* left;
    Node* right;
};

// Utility function to create a new Tree Node
Node* newNode(int val) {
    Node* temp = new Node;
    temp->data = val;
    temp->left = NULL;
    temp->right = NULL;

    return temp;
}

     public:
    int maxPathSum(Node* r)
    {   if(!r) return 0;
        return r->data + max(maxPathSum(r->left),maxPathSum(r->right));
    }};